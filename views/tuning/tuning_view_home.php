        $('.responsive').on('click', '.close', function(){

          $('.close').remove();
          bgColor = $('.active .card-front').css('background-color');
          $('.responsive').removeClass(effect);
          $('.all-content').hide();
          $('.content li').removeClass('active').show().css({ 'border-bottom':'1px solid #2c2c2c',
